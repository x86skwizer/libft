#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	j;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	j = ft_strlen(s1);
	while (j > 0 && ft_strchr(set, s1[j - 1]))
		j--;
	return (ft_substr(s1, 0, j));
}
