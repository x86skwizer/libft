#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dtsize)
{
	size_t	len;

	len = 0;
	if (dtsize > 0)
	{
		while (src[len] && len < dtsize - 1)
		{
			dst[len] = src[len];
			len++;
		}
		dst[len] = '\0';
	}
	return (ft_strlen(src));
}
